//Logical OR (||)
console.log("LOGICAL  OR");
console.log("TRUE || TRUE:", true || true);
console.log("TRUE  || FALSE:", true || false);
let y= a>b || b>a;
console.log('y is: ',y);
console.log("................................");
//...........................................

